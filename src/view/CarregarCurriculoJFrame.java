    private void bBuscaArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscaArquivoActionPerformed
        // TODO add your handling code here:
        final JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fc.showOpenDialog(jLabel1);
        File f = fc.getSelectedFile();
        fileName = f.getParent()+"\\"+f.getName();
        TXCaminhoArquivo.setText(fileName);
        fileType = fc.getTypeDescription(f);  
    }//GEN-LAST:event_bBuscaArquivoActionPerformed

