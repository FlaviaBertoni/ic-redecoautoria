package model;

public class AreaConhecimento {
	
	private String grandeArea = "";
    private String subArea="";
    private String area="";
    private String especialdiade="";
	
    public String getGrandeArea() {
		return grandeArea;
	}
	public void setGrandeArea(String grandeArea) {
		this.grandeArea = grandeArea;
	}
	public String getSubArea() {
		return subArea;
	}
	public void setSubArea(String subArea) {
		this.subArea = subArea;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getEspecialdiade() {
		return especialdiade;
	}
	public void setEspecialdiade(String especialdiade) {
		this.especialdiade = especialdiade;
	}	
}
